/* ============================================================
   THEME TOGGLE
   ============================================================ */
function toggleTheme() {
  const html   = document.documentElement;
  const btn    = document.querySelector('.theme-btn');
  const icon   = document.getElementById('theme-icon');
  const isDark = html.getAttribute('data-theme') === 'dark';

  btn.classList.add('spin');
  setTimeout(() => btn.classList.remove('spin'), 500);

  if (isDark) {
    html.setAttribute('data-theme', 'light');
    icon.className = 'fas fa-moon';
    localStorage.setItem('theme', 'light');
  } else {
    html.setAttribute('data-theme', 'dark');
    icon.className = 'fas fa-sun';
    localStorage.setItem('theme', 'dark');
  }
}

/* ============================================================
   LANGUAGE TOGGLE
   ============================================================ */
function applyLanguage(lang) {
  document.documentElement.lang = lang;

  for (const [id, texts] of Object.entries(translations)) {
    const el = document.getElementById(id);
    if (el) el.innerHTML = texts[lang];
  }

  const flagEl = document.getElementById('flag-icon');
  if (flagEl) {
    flagEl.src = lang === 'es'
      ? 'https://upload.wikimedia.org/wikipedia/en/a/ae/Flag_of_the_United_Kingdom.svg'
      : 'https://upload.wikimedia.org/wikipedia/commons/9/9a/Flag_of_Spain.svg';
    flagEl.alt = lang === 'es' ? 'English' : 'Español';
  }
}

function toggleLanguage() {
  const currentLang = document.documentElement.lang || 'es';
  const newLang = currentLang === 'es' ? 'en' : 'es';
  applyLanguage(newLang);
  localStorage.setItem('lang', newLang);
}

/* ============================================================
   INIT — on DOM ready
   ============================================================ */
document.addEventListener('DOMContentLoaded', function () {

  /* --- Theme --- */
  const savedTheme = localStorage.getItem('theme') || 'light';
  document.documentElement.setAttribute('data-theme', savedTheme);
  const themeIcon = document.getElementById('theme-icon');
  if (themeIcon) {
    themeIcon.className = savedTheme === 'dark' ? 'fas fa-sun' : 'fas fa-moon';
  }

  /* --- Language --- */
  const savedLang = localStorage.getItem('lang') || 'es';
  if (savedLang === 'en') applyLanguage('en');

  /* --- Language bar animation --- */
  setTimeout(() => {
    document.querySelectorAll('.lang-fill').forEach(el => {
      const pct = el.getAttribute('data-width');
      if (pct) el.style.width = pct + '%';
    });
  }, 350);

  /* --- Scroll fade-in for sections --- */
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
      }
    });
  }, {
    root: null, /* always use viewport — body scrolls on desktop too */
    threshold: 0.06,
    rootMargin: '0px 0px -20px 0px',
  });

  document.querySelectorAll('.content-section').forEach((section, index) => {
    section.style.transitionDelay = `${index * 0.08}s`;
    observer.observe(section);
  });

});
